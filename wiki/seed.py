def ensure_seed_pages() -> None:
    """초기 위키 문서가 없으면 샘플 데이터를 채워 넣는다."""
    try:
        existing_titles = set(WikiPage.objects.values_list("title", flat=True))
    except DatabaseError:
        return

    for payload in _SEED_PAGES:
        if payload["title"] in existing_titles:
            continue
        try:
            title = payload["title"]
            defaults = {key: value for key, value in payload.items() if key != "title"}
            WikiPage.objects.get_or_create(title=title, defaults=defaults)
        except DatabaseError:
            continue
