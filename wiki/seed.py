def _generate_bulk_pages():
    pages = []

    def bullet_list(items):
        return "\n".join(f"- {item}" for item in items)

    def join_list(items):
        return ", ".join(items)

    def ensure_text(value, fallback):
        return value if value else fallback

    # 광물
    for data in RESOURCE_DATA:
        for topic in RESOURCE_TOPICS:
            summary = topic["summary"].format(name=data["name"])
            content = topic["content"].format(
                name=data["name"],
                spawn=data["spawn"],
                biome=data["biome"],
                tip=data["tip"],
                automation=data["automation"],
                history=data["history"],
                uses_bullets=bullet_list(data["uses"]),
                uses_joined=join_list(data["uses"]),
            )
            pages.append({
                "title": f"{data['name']} {topic['suffix']}",
                "summary": summary,
                "category": "광물",
                "tags": f"{data['name']},광물,{topic['suffix']}",
                "content": content,
            })

